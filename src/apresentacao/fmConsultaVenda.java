    public boolean verificar(String valor, String quantidade, String data, String id) {

        if (data.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo Data em branco na linha " + id);
            return false;
        }
        if (quantidade.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo Quantidade em branco na linha " + id);
            return false;
        }
        if (valor.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo valor em branco na linha " + id);
            return false;
        }

        return true;
    }

