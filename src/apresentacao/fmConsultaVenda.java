    public void estoques() {
        this.cbEstoque.removeAllItems();
        List<Estoque> lista1 = dao2.listarTodos();
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        for (Estoque e : lista1) {
            modelo.addElement(e.getNomeProduto());
        }

        this.cbEstoque.setModel(modelo);
    }

    public void funcionario() {
        this.cbFuncionario.removeAllItems();
        List<Funcionario> lista2 = dao.listarTodos();
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        for (Funcionario f : lista2) {
            modelo.addElement(f.getNome());
        }

        this.cbFuncionario.setModel(modelo);
    }

    public void mostrarLista() {
        estoques();
        funcionario();
        List<Venda> lista = dao3.listarTodos();
        DefaultTableModel modelo2 = (DefaultTableModel) jTable1.getModel();
        modelo2.setNumRows(0);
        SimpleDateFormat DateFor = new SimpleDateFormat("dd/MM/yyyy");
        for (Venda pro : lista) {
            String stringDate = DateFor.format(pro.getData().getTime());
            funcionario = dao.getByID(pro.getIdFuncionario());
            estoque = dao2.getByID(pro.getIdEstoque());
//            System.out.println(pro.getIdFuncionario());
            modelo2.addRow(
                    new Object[]{pro.getIdVendas().toString(),
                        pro.getQuantidade().toString(),
                        pro.getValor().toString(),
                        stringDate,
                        estoque.getNomeProduto(),
                        funcionario.getNome()
                    });
        }
    }

