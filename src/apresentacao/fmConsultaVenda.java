    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        try {
            int valor = JOptionPane.showConfirmDialog(null, "Tem certeze que deseja salvar?", "Salvar edição", 1);
            if (valor == 0) {
                int tam = jTable1.getRowCount();
                for (int d = 0; d < tam; d++) {

                    String id = (String) jTable1.getValueAt(d, 0);
                    String quantidade = (String) jTable1.getValueAt(d, 1);
                    String val = (String) jTable1.getValueAt(d, 2);
                    String data = (String) jTable1.getValueAt(d, 3);
                    String produto = (String) jTable1.getValueAt(d, 4);
                    String funcionarios = (String) jTable1.getValueAt(d, 5);

                    if (verificar(val, quantidade, data, id)) {
                        List<Funcionario> lista = dao.pesquisar(funcionarios.trim());
                        List<Estoque> lista2 = dao2.pesquisar(produto.trim());

                        try {
                            SimpleDateFormat dat = new SimpleDateFormat("dd/MM/yyyy");
                            Calendar cal = Calendar.getInstance();
                            cal.setTime(dat.parse(data));
                            venda.setData(cal);//4

                        } catch (ParseException ee) {
                            System.out.println(ee);
                        }
                        venda.setValor(Double.parseDouble(val));
                        venda.setQuantidade(Integer.parseInt(quantidade));//  

                        System.out.println(id);

                        venda.setIdVendas(Integer.parseInt(id));//6

                        venda.setIdEstoque(lista2.get(0).getIdEstoque());//8
                        venda.setIdFuncionario(lista.get(0).getIdFuncionario());

                        dao3.altera(venda);
                    } else {
                        JOptionPane.showMessageDialog(null, "Erro em gravar linha " + id);
                        break;
                    }
                }
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                JOptionPane.showMessageDialog(null, "Dados ja foram salvos");
            }
        } catch (HeadlessException r) {
        }
        ((DefaultTableModel) jTable1.getModel()).setRowCount(0);
        mostrarLista();

    }//GEN-LAST:event_btSalvarActionPerformed

